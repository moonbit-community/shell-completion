_moon_modules() {
  local user_path authors mod
  user_path=~/.moon/registry/index/user

  authors=$(ls $user_path)

  _MOON_MODULES=""
  for author in $authors ; do
    for mod_idx in $(ls "$user_path/$author" ); do 
      mod=${mod_idx/.index/}
      _MOON_MODULES+=" $author/$mod"
    done 
  done
}

